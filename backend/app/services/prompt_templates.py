TEMPLATES_BY_ID: dict[str, PromptTemplate] = {t.id: t for t in ALL_TEMPLATES}
TEMPLATES_BY_CATEGORY: dict[str, list[PromptTemplate]] = {}
for t in ALL_TEMPLATES:
    TEMPLATES_BY_CATEGORY.setdefault(t.category, []).append(t)


def get_template(template_id: str) -> Optional[PromptTemplate]:
    return TEMPLATES_BY_ID.get(template_id)


def list_templates(category: Optional[str] = None, role: Optional[str] = None) -> list[PromptTemplate]:
    results = ALL_TEMPLATES
    if category:
        results = [t for t in results if t.category == category]
    if role:
        results = [t for t in results if t.target_role in (role, "all")]
    return results


def render_template(template_id: str, **kwargs: str) -> Optional[str]:
    """Render a prompt template with given variables."""
    tmpl = get_template(template_id)
    if not tmpl:
        return None
    try:
        return tmpl.prompt.format(**kwargs)
    except KeyError:
        return tmpl.prompt
