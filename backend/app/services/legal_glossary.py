# Lookup functions
# ---------------------------------------------------------------------------

ALL_TERMS: dict[str, dict] = {**CRIMINAL_TERMS, **CIVIL_TERMS, **ADMIN_TERMS, **PROCEDURAL_TERMS}

# Build synonym → term mapping
_SYNONYM_MAP: dict[str, str] = {}
for term, info in ALL_TERMS.items():
    for syn in info.get("synonyms", []):
        _SYNONYM_MAP[syn] = term


def lookup(term: str) -> Optional[dict]:
    """Look up a legal term by exact name or synonym."""
    if term in ALL_TERMS:
        return {"term": term, **ALL_TERMS[term]}
    if term in _SYNONYM_MAP:
        canonical = _SYNONYM_MAP[term]
        return {"term": canonical, "matched_synonym": term, **ALL_TERMS[canonical]}
    return None


def search_terms(query: str, limit: int = 5) -> list[dict]:
    """Search legal terms by partial match in name, synonyms, or definition."""
    results = []
    q = query.lower()
    for term, info in ALL_TERMS.items():
        score = 0
        if q in term.lower():
            score += 3
        for syn in info.get("synonyms", []):
            if q in syn.lower():
                score += 2
        if q in info.get("definition", "").lower():
            score += 1
        if q in info.get("en", "").lower():
            score += 1
        if score > 0:
            results.append({"term": term, "score": score, **info})
    results.sort(key=lambda x: x["score"], reverse=True)
    return results[:limit]


def expand_query(query: str) -> str:
    """Expand a query with legal synonyms and statute references.

    Used by Query Rewriting to convert colloquial Thai → legal terms.
    Example: "ถูกโกงเงิน" → "ถูกโกงเงิน ฉ้อโกง ป.อ. มาตรา 341"
    """
    expansions = []
    for term, info in ALL_TERMS.items():
        synonyms = info.get("synonyms", [])
        for syn in synonyms:
            if syn in query and term not in query:
                expansions.append(term)
                if "statute" in info:
                    expansions.append(info["statute"])
                break
        if term in query and "statute" in info:
            statute = info["statute"]
            if statute not in query:
                expansions.append(statute)
    if expansions:
        return query + " " + " ".join(set(expansions))
    return query


def get_all_statutes() -> list[str]:
    """Return all statute references from the glossary."""
    statutes = set()
    for info in ALL_TERMS.values():
        if "statute" in info:
            statutes.add(info["statute"])
    return sorted(statutes)


def get_landmark_cases(topic: Optional[str] = None) -> list[dict]:
    """Return landmark cases, optionally filtered by topic keyword."""
    if topic:
        return [c for c in LANDMARK_CASES if topic in c.get("topic", "") or topic in c.get("principle", "")]
    return LANDMARK_CASES


